type Ctx = typeof AudioContext;

export class Sfx {
  private ctx: AudioContext | null = null;
  private shot: AudioBuffer | null = null;
  private hit: AudioBuffer | null = null;
  private enabled = true;

  /** Create/resume the AudioContext and pre-render both buffers. Call from a user
   *  gesture (click / keypress); safe to call repeatedly. */
  unlock(): void {
    if (!this.enabled) return;
    if (!this.ctx) {
      const Ctor: Ctx | undefined =
        window.AudioContext ??
        (window as unknown as { webkitAudioContext?: Ctx }).webkitAudioContext;
      if (!Ctor) {
        this.enabled = false;
        return;
      }
      this.ctx = new Ctor();
      const sr = this.ctx.sampleRate;
      this.shot = toBuffer(this.ctx, renderDosound(SOUND_SHOT, sr));
      this.hit = toBuffer(this.ctx, renderDosound(SOUND_HIT, sr));
    }
    if (this.ctx.state === 'suspended') void this.ctx.resume();
  }

  playShot(): void {
    this.play(this.shot);
  }

  playHit(): void {
    this.play(this.hit);
  }

  private play(buf: AudioBuffer | null): void {
    if (!this.ctx || !buf || this.ctx.state !== 'running') return;
    const src = this.ctx.createBufferSource();
    src.buffer = buf;
    src.connect(this.ctx.destination);
    src.start();
  }
}

function toBuffer(ctx: AudioContext, pcm: Float32Array): AudioBuffer {
  const buf = ctx.createBuffer(1, pcm.length, ctx.sampleRate);
  buf.getChannelData(0).set(pcm);
  return buf;
}

export const sfx = new Sfx();
